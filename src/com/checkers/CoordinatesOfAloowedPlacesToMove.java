public class CoordinatesOfAloowedPlacesToMove {
    private static int newX;
    private static int newY;

    public CoordinatesOfAloowedPlacesToMove(int newX, int newY) {
        this.newX = newX;
        this.newY = newY;
    }
    public  int getNewX() {
        return newX;
    }
    public  int getNewY() {
        return newY;
    }

}
